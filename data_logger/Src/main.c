/* ================= USER DEFINES ================= */
#define RX_BUF_SIZE    64
#define MAX_SAMPLES    10000
/* =============================================== */

/* UART receive buffer */
static char rxBuf[RX_BUF_SIZE];
static int  rxIndex;

/* Sampling parameters */
static int sampling_rate = 0;   // Hz
static int total_time    = 0;   // seconds
static int num_samples   = 0;

/* Data buffers */
static float voltage_buf[MAX_SAMPLES];
static float current_buf[MAX_SAMPLES];
static float power_buf[MAX_SAMPLES];


int main(void)
{
  /* HAL initialization */
  HAL_Init();
  SystemClock_Config();

  /* Initialize peripherals generated by CubeMX */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_USART2_UART_Init();

  /* Set GPIO outputs PB0, PB1, PB2 LOW */
  HAL_GPIO_WritePin(GPIOB,
                    GPIO_PIN_0 | GPIO_PIN_1 | GPIO_PIN_2,
                    GPIO_PIN_RESET);

  /* Initialize INA228 */
  if (INA228_Init((0x40 << 1), 0.01f) != HAL_OK)
  {
    const char *msg = "INA228 INIT FAILED\n";
    HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY);
  }

  /* ================= Main loop ================= */
  while (1)
  {
    UART_ReceiveLine();

    if (Parse_Command())
    {
      const char ok[] = "OK\n";
      HAL_UART_Transmit(&huart2, (uint8_t*)ok, sizeof(ok)-1, HAL_MAX_DELAY);

      Acquire_Data();
      Transmit_Data();
      /*Transmit_Data_Test();*/
    }
    else
    {
      const char err[] = "ERR\n";
      HAL_UART_Transmit(&huart2, (uint8_t*)err, sizeof(err)-1, HAL_MAX_DELAY);
    }
  }
}

/* ========================================================= */
/* Receive one UART line terminated by '\n'                  */
/* ========================================================= */
static void UART_ReceiveLine(void)
{
  rxIndex = 0;
  memset(rxBuf, 0, sizeof(rxBuf));

  while (1)
  {
    uint8_t byte;
    HAL_UART_Receive(&huart2, &byte, 1, HAL_MAX_DELAY);

    if (byte == '\n')
      break;

    if (rxIndex < RX_BUF_SIZE - 1)
      rxBuf[rxIndex++] = (char)byte;
  }
}

/* ========================================================= */
/* Parse: START,<sampling_rate>,<total_time>                 */
/* ========================================================= */
static int Parse_Command(void)
{
  if (strncmp(rxBuf, "START", 5) != 0)
    return 0;

  char *tok = strtok(rxBuf, ",");   // START
  tok = strtok(NULL, ",");
  if (!tok) return 0;
  sampling_rate = atoi(tok);

  tok = strtok(NULL, ",");
  if (!tok) return 0;
  total_time = atoi(tok);

  if (sampling_rate <= 0 || total_time <= 0)
    return 0;

  num_samples = sampling_rate * total_time;
  if (num_samples > MAX_SAMPLES)
    num_samples = MAX_SAMPLES;

  return 1;
}

/* ========================================================= */
/* Acquire data from INA228                                  */
/* ========================================================= */
static void Acquire_Data(void)
{
  uint8_t healthy = 0;
  INA228_CheckHealth((0x40 << 1), &healthy);

  if (!healthy)
  {
    for (int i = 0; i < num_samples; i++)
      voltage_buf[i] = current_buf[i] = power_buf[i] = 0.0f;
    return;
  }

  uint32_t delay_ms = (sampling_rate > 0) ? (1000 / sampling_rate) : 1;

  for (int i = 0; i < num_samples; i++)
  {
    float v = 0.0f, c = 0.0f, p = 0.0f;

    INA228_ReadVoltage((0x40 << 1), &v);
    INA228_ReadCurrent((0x40 << 1), &c);
    INA228_ReadPower  ((0x40 << 1), &p);

    voltage_buf[i] = v;
    current_buf[i] = c;
    power_buf[i]   = p;

    HAL_Delay(delay_ms);
  }
}

/* ========================================================= */
/* Transmit data to PC                                       */
/* ========================================================= */
static void Transmit_Data(void)
{
  char line[64];

  for (int i = 0; i < num_samples; i++)
  {
    int len = snprintf(line, sizeof(line),
                       "%.6f,%.6f,%.6f\n",
                       voltage_buf[i],
                       current_buf[i],
                       power_buf[i]);

    HAL_UART_Transmit(&huart2, (uint8_t*)line, len, HAL_MAX_DELAY);
  }

  const char done[] = "DONE\n";
  HAL_UART_Transmit(&huart2, (uint8_t*)done, sizeof(done)-1, HAL_MAX_DELAY);
}

